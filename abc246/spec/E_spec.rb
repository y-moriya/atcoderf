RSpec.describe 'test' do
  it 'test with "5\n1 3\n3 5\n....#\n...#.\n.....\n.#...\n#....\n"' do
    io = IO.popen("ruby abc246/E.rb", "w+")
    io.puts("5\n1 3\n3 5\n....#\n...#.\n.....\n.#...\n#....\n")
    io.close_write
    expect(io.readlines.join).to eq("3\n")
  end

  it 'test with "4\n3 2\n4 2\n....\n....\n....\n....\n"' do
    io = IO.popen("ruby abc246/E.rb", "w+")
    io.puts("4\n3 2\n4 2\n....\n....\n....\n....\n")
    io.close_write
    expect(io.readlines.join).to eq("-1\n")
  end

  it 'test with "18\n18 1\n1 18\n..................\n.####.............\n.#..#..####.......\n.####..#..#..####.\n.#..#..###...#....\n.#..#..#..#..#....\n.......####..#....\n.............####.\n..................\n..................\n.####.............\n....#..#..#.......\n.####..#..#..####.\n.#.....####..#....\n.####.....#..####.\n..........#..#..#.\n.............####.\n..................\n"' do
    io = IO.popen("ruby abc246/E.rb", "w+")
    io.puts("18\n18 1\n1 18\n..................\n.####.............\n.#..#..####.......\n.####..#..#..####.\n.#..#..###...#....\n.#..#..#..#..#....\n.......####..#....\n.............####.\n..................\n..................\n.####.............\n....#..#..#.......\n.####..#..#..####.\n.#.....####..#....\n.####.....#..####.\n..........#..#..#.\n.............####.\n..................\n")
    io.close_write
    expect(io.readlines.join).to eq("9\n")
  end

end
